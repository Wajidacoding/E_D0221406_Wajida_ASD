#operator penugasan

# sama dengan(=)
a = 60
print(a)

# tambah sama dengan (+=)
a += 10
print(a)

#kurang sama dengan(-=)
a -= 4
print(a)

#kali  sama dengan (*=)
a *= 5
print(a)

#bagi sama dengan (/=)
a /= 4
print(a)

#sisa bangi(%=)
a %= 3
print(a)

# pangkat sama dengan(**=)
a **= 10
print(a)

#pembagian bilangan bullat sama dengan(//=)
a //= 5
print(a)



